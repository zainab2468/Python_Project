#math operation
x = 50 
y = 25

sum = x + y
print(f"The sum of two number is : {sum}")

subtract = x - y
print(f"The difference of the two number is : {subtract}")

product = x * y
print(f"The product of the two number is : {product}")

try :
    result = x // y
    print(result)
except:
    print("an error occured")




